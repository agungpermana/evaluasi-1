package com.evaluasi1;
/**
 *
 * @author AP
 */
public class LatihanException extends Exception {

    public LatihanException() {
    }

    public LatihanException(String message) {
        super(message);
    }

    public LatihanException(String message, Throwable cause) {
        super(message, cause);
    }

    public LatihanException(Throwable cause) {
        super(cause);
    }

    public LatihanException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
